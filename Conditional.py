# Shortcut for if-else statement (Ternary operator)
# x if condition else y

n = 5
a = 10
age = 18
temp = 10
user_role = "admin"
print("Positive" if n > 0 else "Negative")
print("Even" if n%2 == 0 else "Odd")
print(a if a > n else n)
print("Adult" if age >= 18 else "Child")
print("Hot" if temp > 25 else "Cold")
print("Full access" if user_role == "admin" else "Limited access")
